import torch

# ??????? ??????? A (3x4) ? B (4x3) ? ????????????? seed ??? ?????????????????
torch.manual_seed(42)
A = torch.rand(3, 4)
B = torch.rand(4, 3)

print("Tensor A (3x4):\n", A)
print("Tensor B (4x3):\n", B)

# 1. ???????????????? ??????? A ? A^T (4x3)
A_transposed = A.T  # ??? A.transpose(0, 1)
print("\n1. A ????????????????? (4x3):\n", A_transposed)

# 2. ????????? ????????? A @ B ? (3x4) @ (4x3) = (3x3)
matrix_product = A @ B  # torch.matmul(A, B)
print("\n2. ????????? A @ B (3x3):\n", matrix_product)

# 3. ???????????? ????????? A * B^T ? (3x4) * (3x4)
B_transposed = B.T  # B ????????????????? (3x4)
elementwise_product = A * B_transposed
print("\n3. ???????????? A * B^T (3x4):\n", elementwise_product)

# 4. ????? ???? ????????? ??????? A
sum_A = A.sum()
