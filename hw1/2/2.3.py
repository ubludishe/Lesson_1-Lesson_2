import torch

x = torch.tensor(2.0, requires_grad=True)

# f(x) = sin(x^2 + 1)
inner = x**2 + 1
f = torch.sin(inner)

print("f(x) =", f.item())

# ?????? 1: ??????????? backward()
f.backward()
print("df/dx (backward()) =", x.grad.item())

# ?????? 2: ??????????? ??????? ??? autograd.grad
x.grad.zero_()  # ???????? ????????
inner2 = x**2 + 1
f2 = torch.sin(inner2)
df_dx = torch.autograd.grad(f2, x, grad_outputs=torch.tensor(1.0), create_graph=False)[0]
print("df/dx (autograd.grad) =", df_dx.item())