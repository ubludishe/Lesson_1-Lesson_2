import torch
import time


def measure_time(func, device='cpu', num_runs=10, warmup_runs=3):
    """
    ???????? ????? ?????????? ??????? ?? CPU ??? GPU

    Args:
        func: ??????? ??? ??????
        device: 'cpu' ??? 'cuda'
        num_runs: ?????????? ???????
        warmup_runs: ?????????? ?????????
    """
    if device == 'cuda' and not torch.cuda.is_available():
        print("CUDA ??????????, ?????????? CPU")
        device = 'cpu'

    # ???????
    for _ in range(warmup_runs):
        func()
        if device == 'cuda':
            torch.cuda.synchronize()

    # ???????? ??????
    times = []
    for _ in range(num_runs):
        if device == 'cuda':
            start = torch.cuda.Event(enable_timing=True)
            end = torch.cuda.Event(enable_timing=True)
            start.record()
            func()
            end.record()
            torch.cuda.synchronize()
            times.append(start.elapsed_time(end))
        else:
            start = time.time()
            func()
            times.append((time.time() - start) * 1000)  # ? ?????????????

    return sum(times) / len(times)


# ?????? ?????????????
def matrix_multiply():
    a = torch.randn(1024, 1024)
    b = torch.randn(1024, 1024)
    return a @ b


# ????? ?? CPU
cpu_time = measure_time(matrix_multiply, 'cpu')
print(f"CPU ?????: {cpu_time:.2f} ??")

# ????? ?? GPU (???? ????????)
if torch.cuda.is_available():
    gpu_time = measure_time(matrix_multiply, 'cuda')
    print(f"GPU ?????: {gpu_time:.2f} ??")
    print(f"?????????: {cpu_time / gpu_time:.1f}x")